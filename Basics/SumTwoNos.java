import java.util.*;


class SumTwoNos{
public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter First Number:-");
        int a = sc.nextInt();
        System.out.print("Enter Second Number:-");
        int b = sc.nextInt();

        System.out.println("Sum Of two Number is :-"+ (a+b));
    }
}